// Master generator
function generatePage(route: string, title: string, description: string, schemaType: string, contentHtml: string, extraSchemaData: any = {}) {
  // Construct absolute file path
  let dirPath = DIST_DIR;
  let fileName = 'index.html';

  if (route !== '') {
    dirPath = path.join(DIST_DIR, route);
    // Create directory recursively
    if (!fs.existsSync(dirPath)) {
      fs.mkdirSync(dirPath, { recursive: true });
    }
  }

  const filePath = path.join(dirPath, fileName);

  // Parse layout active route key
  let activeRouteKey = '';
  if (route.startsWith('categories')) activeRouteKey = 'categories';
  else if (route.startsWith('blog')) activeRouteKey = 'blog';
  else if (route.startsWith('compare')) activeRouteKey = 'compare';
  else if (route.startsWith('about')) activeRouteKey = 'about';

  // Inject content and metadata into index.html template
  const completeBodyHtml = wrapInLayout(activeRouteKey, contentHtml);

  // Assemble dynamic head elements
  const canonicalTag = `<link rel="canonical" href="${BASE_URL}/${route}" />`;
  const schemaTags = generateSchema(schemaType, route, title, description, extraSchemaData);

  // Replace placeholders in Vite index.html template
  let pageHtml = template;

  // 1. Replace Title
  pageHtml = pageHtml.replace(/<title>.*?<\/title>/, `<title>${title}</title>`);

  // 2. Inject meta description or replace existing
  if (pageHtml.includes('name="description"')) {
    pageHtml = pageHtml.replace(/<meta name="description" content=".*?"\s*\/?>/, `<meta name="description" content="${description}" />`);
  } else {
    pageHtml = pageHtml.replace('</head>', `  <meta name="description" content="${description}" />\n</head>`);
  }

  // 3. Inject canonical and schemas in head
  const headAdditions = `\n  ${canonicalTag}\n  ${schemaTags}\n`;
  pageHtml = pageHtml.replace('</head>', `${headAdditions}</head>`);

  // 4. Inject content into <div id="root"></div>
  pageHtml = pageHtml.replace('<div id="root"></div>', `<div id="root">${completeBodyHtml}</div>`);

  // Write file out
  fs.writeFileSync(filePath, pageHtml, 'utf-8');
  console.log(`Prerendered: /${route} -> ${filePath}`);
}

