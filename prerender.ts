// Helper to convert basic Markdown to semantic HTML for guides
function markdownToHtml(md: string): string {
  let html = md.trim();

  // Convert headings
  html = html.replace(/^### (.*$)/gim, '<h3 class="font-display text-lg font-bold text-slate-900 dark:text-white pt-5 pb-2">$1</h3>');
  html = html.replace(/^## (.*$)/gim, '<h2 class="font-display text-xl font-bold text-slate-900 dark:text-white pt-6 pb-2">$1</h2>');
  html = html.replace(/^# (.*$)/gim, '<h1 class="font-display text-2xl font-extrabold text-slate-900 dark:text-white pt-8 pb-3">$1</h1>');

  // Convert lists
  // Unordered lists
  html = html.replace(/^\* (.*$)/gim, '<li class="text-sm text-slate-600 dark:text-slate-350 py-1 leading-relaxed">$1</li>');
  // Wrap list items in <ul> if needed (simplified block-level wrapping)
  
  // Convert Bold markdown
  html = html.replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');

  // Convert equations (e.g. $$A = P...$$)
  html = html.replace(/\$\$(.*?)\$\$/g, '<div class="my-5 p-4 bg-slate-100 dark:bg-slate-800 rounded-xl border border-slate-200 dark:border-slate-700 font-mono text-center text-sm text-slate-800 dark:text-slate-200 overflow-x-auto">$1</div>');
  html = html.replace(/\$(.*?)\$/g, '<code class="px-1.5 py-0.5 bg-slate-100 dark:bg-slate-800 rounded font-mono text-xs">$1</code>');

  // Convert double newlines to paragraphs
  const paragraphs = html.split(/\n\s*\n/);
  const formattedParagraphs = paragraphs.map(p => {
    p = p.trim();
    if (!p) return '';
    if (p.startsWith('<h') || p.startsWith('<li') || p.startsWith('<div')) {
      return p;
    }
    // If it contains list items, wrap them in <ul>
    if (p.startsWith('<li')) {
      return `<ul class="list-disc pl-5 my-3 space-y-1">${p}</ul>`;
    }
    return `<p class="text-sm sm:text-base text-slate-600 dark:text-slate-300 leading-relaxed mb-4">${p}</p>`;
  });

  return formattedParagraphs.filter(Boolean).join('\n');
}

// Helper to generate dynamic JSON-LD schemas
function generateSchema(type: string, route: string, title: string, description: string, extra: any = {}): string {
  const schemas: any[] = [
    {
      '@context': 'https://schema.org',
      '@type': 'Organization',
      '@id': 'https://moneymetrichubs.com/#organization',
      'name': 'Money Metric Hubs',
      'url': 'https://moneymetrichubs.com',
      'logo': 'https://moneymetrichubs.com/logo.png',
      'description': 'Premium finance calculator platform for smart financial decisions.',
      'sameAs': [
        'https://twitter.com/moneymetricshub',
        'https://github.com/moneymetricshub'
      ]
    }
  ];

  if (type === 'home') {
    schemas.push({
      '@context': 'https://schema.org',
      '@type': 'WebSite',
      '@id': 'https://moneymetrichubs.com/#website',
      'url': 'https://moneymetrichubs.com',
      'name': 'Money Metric Hubs',
      'description': 'Smarter Financial Decisions Through Better Numbers',
      'potentialAction': {
        '@type': 'SearchAction',
        'target': 'https://moneymetrichubs.com/search?q={search_term_string}',
        'query-input': 'required name=search_term_string'
      }
    });
  } else if (type === 'calculator') {
    schemas.push({
      '@context': 'https://schema.org',
      '@type': 'WebApplication',
      '@id': `${BASE_URL}/${route}#webapp`,
      'name': title.split('|')[0].trim(),
      'url': `${BASE_URL}/${route}`,
      'applicationCategory': 'BusinessApplication',
      'operatingSystem': 'All',
      'browserRequirements': 'Requires JavaScript',
      'offers': {
        '@type': 'Offer',
        'price': '0',
        'priceCurrency': 'USD'
      }
    });

    schemas.push({
      '@context': 'https://schema.org',
      '@type': 'BreadcrumbList',
      'itemListElement': [
        { '@type': 'ListItem', 'position': 1, 'name': 'Home', 'item': BASE_URL },
        { '@type': 'ListItem', 'position': 2, 'name': extra.categoryName || 'Calculators', 'item': `${BASE_URL}/categories/${extra.categorySlug || 'investing'}` },
        { '@type': 'ListItem', 'position': 3, 'name': title.split('|')[0].trim(), 'item': `${BASE_URL}/${route}` }
      ]
    });

    if (extra.faqs && extra.faqs.length > 0) {
      schemas.push({
        '@context': 'https://schema.org',
        '@type': 'FAQPage',
        'mainEntity': extra.faqs.map((faq: any) => ({
          '@type': 'Question',
          'name': faq.question,
          'acceptedAnswer': { '@type': 'Answer', 'text': faq.answer }
        }))
      });
    }
  } else if (type === 'article' || type === 'guide') {
    schemas.push({
      '@context': 'https://schema.org',
      '@type': 'Article',
      '@id': `${BASE_URL}/${route}#article`,
      'headline': title.split('|')[0].trim(),
      'description': description,
      'url': `${BASE_URL}/${route}`,
      'publisher': {
        '@type': 'Organization',
        'name': 'Money Metric Hubs',
        'logo': { '@type': 'ImageObject', 'url': 'https://moneymetrichubs.com/logo.png' }
      },
      'author': {
        '@type': 'Organization',
        'name': 'Money Metric Hubs Editorial Team'
      }
    });

    if (extra.faqs && extra.faqs.length > 0) {
      schemas.push({
        '@context': 'https://schema.org',
        '@type': 'FAQPage',
        'mainEntity': extra.faqs.map((faq: any) => ({
          '@type': 'Question',
          'name': faq.question,
          'acceptedAnswer': { '@type': 'Answer', 'text': faq.answer }
        }))
      });
    }
  }

  return schemas.map(schema => `<script type="application/ld+json" class="mmh-schema">${JSON.stringify(schema)}</script>`).join('\n  ');
}

